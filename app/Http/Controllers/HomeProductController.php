    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $product = Product::find($id);
        $products = Product::where('category_id', $product->category_id)->where('id', '!=', $product->id)->limit(3)->get();
        $cart = Cart::where('product_id', $product->id)->where('user_id', auth()->user()->id)->first(['id', 'amount']);
        $categories = Category::all();
        return view('home.product', compact('product', 'products', 'cart', 'categories'));
    }

